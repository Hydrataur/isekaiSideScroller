public class Distance {

    int xDis;
    int yDis;

    public Distance(){
        this.xDis = 0;
        this.yDis = 0;
    }

    public int getxDis() {
        return xDis;
    }

    public void setxDis(int xDis) {
        this.xDis = xDis;
    }

    public int getyDis() {
        return yDis;
    }

    public void setyDis(int yDis) {
        this.yDis = yDis;
    }
}
